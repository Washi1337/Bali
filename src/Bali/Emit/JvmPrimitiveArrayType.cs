namespace Bali.Emit
{
    /// <summary>
    /// The 8 "real" primitive types that the JVM supports.
    /// </summary>
    public enum JvmPrimitiveArrayType
    {
        /// <summary>
        /// The <c>boolean</c> primitive.
        /// </summary>
        Boolean = 4,

        /// <summary>
        /// The <c>char</c> primitive.
        /// </summary>
        Char = 5,

        /// <summary>
        /// The <c>float</c> primitive.
        /// </summary>
        Float = 6,

        /// <summary>
        /// The <c>double</c> primitive.
        /// </summary>
        Double = 7,

        /// <summary>
        /// The <c>byte</c> primitive.
        /// </summary>
        Byte = 8,

        /// <summary>
        /// The <c>short</c> primitive.
        /// </summary>
        Short = 9,

        /// <summary>
        /// The <c>int</c> primitive.
        /// </summary>
        Int = 10,

        /// <summary>
        /// The <c>long</c> primitive.
        /// </summary>
        Long = 11
    }
}